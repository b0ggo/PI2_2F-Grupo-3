export const obterEstadosPorCodigo = () => ESTADOS_BRASILEIROS;

export const obterEstadoPorSigla = (codigo) => {
  if (!codigo) return null;
  return ESTADOS_BRASILEIROS.find(estado => estado.code === codigo.toUpperCase());
};

export const ehSiglaEstadoValida = (codigo) => {
  if (!codigo || typeof codigo !== 'string') return false;
  return ESTADOS_BRASILEIROS.some(estado => estado.code === codigo.toUpperCase());
};
